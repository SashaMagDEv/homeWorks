function showBiography() {
    let name = prompt("Введіть ваше ім'я:");
    let age = prompt("Введіть ваш вік:");
    let city = prompt("Введіть ваше місто:");

  alert(`Добрий день, мене звати ${name} мені ${age}, я живу в місті ${city}`);
}